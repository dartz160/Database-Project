
package model;


public class FichaMedica {
    
    
    private int idFichaMedica;
    private String Alergias;
    private String Tipo_Sangre;
    private String Antecedentes;
    private String Observacion;
    private String Persona_DNI;

    public int getIdFichaMedica() {
        return idFichaMedica;
    }

    public void setIdFichaMedica(int idFichaMedica) {
        this.idFichaMedica = idFichaMedica;
    }

    public String getAlergias() {
        return Alergias;
    }

    public void setAlergias(String Alergias) {
        this.Alergias = Alergias;
    }

    public String getTipo_Sangre() {
        return Tipo_Sangre;
    }

    public void setTipo_Sangre(String Tipo_Sangre) {
        this.Tipo_Sangre = Tipo_Sangre;
    }

    public String getAntecedentes() {
        return Antecedentes;
    }

    public void setAntecedentes(String Antecedentes) {
        this.Antecedentes = Antecedentes;
    }

    public String getObservacion() {
        return Observacion;
    }

    public void setObservacion(String Observacion) {
        this.Observacion = Observacion;
    }

    public String getPersona_DNI() {
        return Persona_DNI;
    }

    public void setPersona_DNI(String Persona_DNI) {
        this.Persona_DNI = Persona_DNI;
    }
    
    
}
